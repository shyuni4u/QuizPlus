package ety;

public class IdolEty {
	private long id;
	private String image;
	private int image_ok;
	private String name;
	private String info;
	private String color;
	private int isplus;
	private long count;
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public int getImage_ok() {
		return image_ok;
	}
	public void setImage_ok(int image_ok) {
		this.image_ok = image_ok;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getInfo() {
		return info;
	}
	public void setInfo(String info) {
		this.info = info;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public int getIsplus() {
		return isplus;
	}
	public void setIsplus(int isplus) {
		this.isplus = isplus;
	}
	public long getCount() {
		return count;
	}
	public void setCount(long count) {
		this.count = count;
	}
}
